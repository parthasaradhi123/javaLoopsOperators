
public class SwitchCase {

	public static void main(String[] args)
	{
		int number = 100;
		
		switch(number)
		{
			case 17: System.out.println("1 case");
					 break;	 
			case 13: System.out.println("2 case");
					 break;
			case 10: System.out.println("3 case");
						break;
			case 11: System.out.println("4 case");
						break;
			case 15: System.out.println("5 case");
						break;
			default : System.out.println("Nothing matches");
			 
			 
		}

	}

}
