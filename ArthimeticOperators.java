
public class ArthimeticOperators {

	public static void main(String[] args) 
	{
		 int a = 38;
		 int b = 5;
		 int add = a+b;
		 int sub = a-b;
		 int div = a/b; // here the truncation will happen the values after dot will not appear in output.
		 int modu = a%b;
		 int mal = a*b;
		 
		 System.out.println("Addition of two numbers result :: "+add);
		 System.out.println("Subtraction of two numbers result :: " +sub);
		 System.out.println("Division of two numbers results :: "+div);
		 System.out.println("Modulo of two numbers which will give remainder as output :: "+modu);
		 System.out.println("Multiplication of two numbers :: "+mal);

	}

}
